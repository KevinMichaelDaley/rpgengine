/**
 * @file clay_fonts.c
 * @brief Font loading and glyph atlas generation.
 *
 * Uses packed Terminus 6x12 bitmap font data to generate a single-channel
 * GL texture atlas. Each printable ASCII character (32-126) occupies a
 * 6x12 pixel cell in the atlas.
 *
 * Non-static functions: 3 (init, destroy, char_width).
 */

#include "ferrum/editor/ui/clay_fonts.h"
#include "ferrum/editor/ui/clay_theme.h"
#include "ferrum/renderer/gl_constants.h"

#include <string.h>
#include <stddef.h>

/**
 * @brief Load a GL function pointer via memcpy to avoid ISO C
 *        pedantic warning about void* to function pointer conversion.
 */
#define LOAD_GL_PROC(dst, loader_ptr, name)                   \
    do {                                                       \
        void *raw_ = (loader_ptr)->get_proc_address(           \
            (name), (loader_ptr)->user_data);                  \
        memcpy(&(dst), &raw_, sizeof(dst));                    \
    } while (0)

/* ---- Atlas dimensions ---- */

/** 16 characters per row, 6 pixels each = 96 pixels wide. */
#define ATLAS_W 96
/** 6 rows of 12 pixels each = 72 pixels tall. */
#define ATLAS_H 72
/** Number of printable ASCII characters (32-126). */
